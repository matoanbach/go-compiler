func runCompilerTests(t *testing.T, tests []compilerTestCase) {
	t.Helper()

	// for each test case
	// init the Compiler
	// parse to get the AST
	// run the compiler
	// get the bytecode

	// call testInstructions() to check the generated instructions
	// if err, then return t.Fatalf("testInstructions failed: %s", err)
	// call testConstants() to check the generated constants
	// if err, then return t.Fatalf("testInstructions failed: %s", err)
	for _, tt := range tests {
		program := parse(tt.input)

		compiler := New()
		err := compiler.Compile(program)
		if err != nil {
			t.Fatalf("compiler error: %s", err)
		}

		bytecode := compiler.Bytecode()

		err = testInstructions(tt.expectedInstructions, bytecode.Instructions)
		if err != nil {
			t.Fatalf("testInstructions failed: %s", err)
		}

		err = testConstants(t, tt.expectedConstants, bytecode.Constants)
		if err != nil {
			t.Fatalf("testConstants failed: %s", err)
		}
	}
}

func parse(input string) *ast.Program {
	l := lexer.New(input)
	p := parser.New(l)
	return p.ParseProgram()
}

func concatInstructions(instructions []code.Instructions) code.Instructions {
	output := code.Instructions{}

	for _, ins := range instructions {
		output = append(output, ins...)
	}

	return output
}

func testInstructions(expected []code.Instructions, actual code.Instructions) error {
	concatted := concatInstructions(expected)
	if len(concatted) != len(actual) {
		return fmt.Errorf("wrong number of instructions. \nexpected=%q\n got=%q\n", concatted, actual)
	}

	for i, ins := range concatted {
		if ins != actual[i] {
			fmt.Printf("expected: %q\n\ngot: %q", concatted, actual)
			return fmt.Errorf("wrong instruction at %d. \nexpected=%v\n got=%v\n", i, concatted[i], expected[i])
		}
	}

	return nil
}

func testConstants(t *testing.T, expected []interface{}, actual []object.Object) error {
	if len(expected) != len(actual) {
		return fmt.Errorf("wrong number of constants. \nexpected=%d, %q\n got=%d\n", len(expected), expected, len(actual))
	}

	for i, constant := range expected {
		switch constant := constant.(type) {
		case int:
			err := testIntegerObject(int64(constant), actual[i])
			if err != nil {
				return fmt.Errorf("constant %d - testIntegerObject failed: %s", i, err)
			}
		case string:
			err := testStringObject(string(constant), actual[i])
			if err != nil {
				return fmt.Errorf("constant %d - testStringObject failed: %s", i, err)
			}
		}
	}

	return nil
}

func testStringObject(expected string, actual object.Object) error {
	result, ok := actual.(*object.String)
	if !ok {
		return fmt.Errorf("object is not String. got=%T (%+v)", actual, actual)
	}

	if expected != result.Value {
		return fmt.Errorf("object has the wrong value. expected=%s, got=%s", expected, result.Value)
	}

	return nil
}

func testIntegerObject(expected int64, actual object.Object) error {
	result, ok := actual.(*object.Integer)

	if !ok {
		return fmt.Errorf("object is not Integer. got=%T (%+v)", actual, actual)
	}

	if expected != result.Value {
		return fmt.Errorf("object has the wrong value. expected=%d, got=%d", expected, result.Value)
	}

	return nil
}
